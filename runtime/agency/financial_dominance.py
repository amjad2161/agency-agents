    def detect_market_regime(self) -> str:
        """
        Detect current market regime classification.

        Analyzes price action, volatility, and volume patterns to
        classify the market as bull/bear/sideways and assess
        volatility and liquidity regimes.

        Returns:
            String describing the detected market regime
        """
        logger.info(f"[{self.name}] Detecting market regime...")

        # Generate mock market data for regime detection
        prices = _generate_mock_prices(4500.0, 90, 0.012)
        volumes = [random.randint(2000000000, 5000000000) for _ in range(90)]

        # Trend detection
        sma_20 = _mean(prices[-20:]) if len(prices) >= 20 else _mean(prices)
        sma_50 = _mean(prices[-50:]) if len(prices) >= 50 else _mean(prices)
        current = prices[-1]

        if current > sma_20 > sma_50 * 1.02:
            trend_regime = "bull_market"
        elif current < sma_20 < sma_50 * 0.98:
            trend_regime = "bear_market"
        else:
            trend_regime = "sideways_market"

        # Volatility regime
        returns = [(prices[i] / prices[i - 1]) - 1 for i in range(1, len(prices))]
        vol = _std(returns[-20:]) * math.sqrt(252) if len(returns) >= 20 else 0.15

        if vol > 0.25:
            vol_regime = "high_volatility"
        elif vol > 0.15:
            vol_regime = "normal_volatility"
        else:
            vol_regime = "low_volatility"

        # Liquidity regime
        avg_volume = _mean(volumes[-20:]) if len(volumes) >= 20 else _mean(volumes)
        if avg_volume > 3500000000:
            liq_regime = "high_liquidity"
        elif avg_volume > 2500000000:
            liq_regime = "normal_liquidity"
        else:
            liq_regime = "low_liquidity"

        regime = f"{trend_regime}_{vol_regime}_{liq_regime}"
        self._data_cache["regime"] = regime

        logger.info(f"[{self.name}] Regime: {regime}")
        return regime

    def predict_volatility(
        self,
        symbol: str,
        horizon: int = 30,
    ) -> VolatilityForecast:
        """
        Predict future volatility using GARCH-inspired modeling.

        Models volatility clustering and mean reversion to forecast
        future volatility over the specified horizon.

        Args:
            symbol: Ticker symbol to forecast
            horizon: Forecast horizon in trading days

        Returns:
            VolatilityForecast: Predicted volatility and confidence intervals
        """
        logger.info(f"[{self.name}] Forecasting volatility for {symbol} ({horizon}d horizon)...")

        # Generate historical returns
        prices = _generate_mock_prices(100.0, 252, 0.02)
        returns = [(prices[i] / prices[i - 1]) - 1 for i in range(1, len(prices))]

        # Calculate current volatility
        current_vol = _std(returns[-20:]) * math.sqrt(252) if len(returns) >= 20 else 0.20

        # GARCH-inspired forecast (simplified)
        # vol_t+1 = omega + alpha * r_t^2 + beta * vol_t
        omega = 0.000001
        alpha = 0.10
        beta = 0.85
        long_run_var = omega / (1 - alpha - beta) if (alpha + beta) < 1 else 0.0001

        forecasted_vols = []
        vol_pred = current_vol / math.sqrt(252)  # Daily vol
        for _ in range(horizon):
            last_return_sq = returns[-1] ** 2 if returns else 0.0001
            vol_pred = math.sqrt(omega + alpha * last_return_sq + beta * vol_pred ** 2)
            forecasted_vols.append(vol_pred * math.sqrt(252))

        forecasted_vol = _mean(forecasted_vols) if forecasted_vols else current_vol

        # Volatility trend
        if forecasted_vol > current_vol * 1.1:
            trend = "increasing"
        elif forecasted_vol < current_vol * 0.9:
            trend = "decreasing"
        else:
            trend = "stable"

        # Volatility cone (percentiles of historical vol)
        rolling_vols = []
        for i in range(20, len(returns)):
            window = returns[i - 20:i]
            rolling_vols.append(_std(window) * math.sqrt(252))

        cone = [
            _percentile(rolling_vols, 0.10) if rolling_vols else current_vol * 0.7,
            _percentile(rolling_vols, 0.25) if rolling_vols else current_vol * 0.85,
            _percentile(rolling_vols, 0.50) if rolling_vols else current_vol,
            _percentile(rolling_vols, 0.75) if rolling_vols else current_vol * 1.15,
            _percentile(rolling_vols, 0.90) if rolling_vols else current_vol * 1.3,
        ]

        ci = {
            "lower_90": round(current_vol * 0.7, 4),
            "upper_90": round(current_vol * 1.3, 4),
            "lower_95": round(current_vol * 0.6, 4),
            "upper_95": round(current_vol * 1.4, 4),
        }

        forecast = VolatilityForecast(
            symbol=symbol,
            forecast_horizon=horizon,
            current_volatility=round(current_vol, 4),
            forecasted_volatility=round(forecasted_vol, 4),
            volatility_trend=trend,
            volatility_cone=[round(v, 4) for v in cone],
            confidence_intervals=ci,
        )

        logger.info(f"[{self.name}] Vol forecast: current={current_vol:.2%}, "
                    f"forecast={forecasted_vol:.2%}, trend={trend}")
        return forecast

