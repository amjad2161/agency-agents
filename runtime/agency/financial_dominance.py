    def close_position(self, symbol: str, portfolio: Portfolio,
                       exit_price: Optional[float] = None) -> dict:
        """
        Close an open position (paper trade).

        Args:
            symbol: Symbol to close
            portfolio: Portfolio containing the position
            exit_price: Optional override exit price

        Returns:
            dict with closing details
        """
        if symbol not in portfolio.positions:
            return {"status": "error", "reason": f"No position in {symbol}"}

        pos = portfolio.positions[symbol]
        close_price = exit_price or pos["entry_price"] * (1 + random.uniform(-0.05, 0.08))

        if pos["direction"] == "long":
            pnl = (close_price - pos["entry_price"]) * pos["quantity"]
        else:
            pnl = (pos["entry_price"] - close_price) * pos["quantity"]

        portfolio.cash += pos["position_value"] + pnl
        portfolio.total_pnl += pnl
        portfolio.daily_pnl += pnl

        result = {
            "symbol": symbol,
            "direction": pos["direction"],
            "entry": pos["entry_price"],
            "exit": round(close_price, 2),
            "quantity": pos["quantity"],
            "pnl": round(pnl, 2),
            "status": "closed",
            "timestamp": datetime.now().isoformat(),
        }
        portfolio.trade_history.append(result)
        del portfolio.positions[symbol]
        portfolio.total_value = portfolio.cash + sum(
            p.get("position_value", 0) for p in portfolio.positions.values()
        )

        logger.info(f"[{self.name}] Position closed: {symbol} P&L=${pnl:,.2f}")
        return result


