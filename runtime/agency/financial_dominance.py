def _mean(values: List[float]) -> float:
    """Pure Python mean calculation."""
    if not values:
        return 0.0
    return sum(values) / len(values)


def _std(values: List[float]) -> float:
    """Pure Python standard deviation (population)."""
    if len(values) < 2:
        return 0.0
    m = _mean(values)
    variance = sum((x - m) ** 2 for x in values) / len(values)
    return math.sqrt(variance)


def _ema(values: List[float], period: int) -> List[float]:
    """Calculate Exponential Moving Average."""
    if len(values) < period:
        return values[:]
    multiplier = 2.0 / (period + 1)
    ema_values = [sum(values[:period]) / period]
    for price in values[period:]:
        ema_values.append((price - ema_values[-1]) * multiplier + ema_values[-1])
    return ema_values


def _rsi(prices: List[float], period: int = 14) -> float:
    """Calculate Relative Strength Index (RSI) from price series."""
    if len(prices) < period + 1:
        return 50.0
    deltas = [prices[i] - prices[i - 1] for i in range(1, len(prices))]
    gains = [max(d, 0) for d in deltas]
    losses = [abs(min(d, 0)) for d in deltas]
    avg_gain = sum(gains[:period]) / period
    avg_loss = sum(losses[:period]) / period
    if avg_loss == 0:
        return 100.0
    rs = avg_gain / avg_loss
    return 100.0 - (100.0 / (1.0 + rs))


def _macd(prices: List[float]) -> Tuple[float, float, float]:
    """Calculate MACD line, signal line, and histogram."""
    ema_12 = _ema(prices, 12)
    ema_26 = _ema(prices, 26)
    if len(ema_12) == 0 or len(ema_26) == 0:
        return 0.0, 0.0, 0.0
    # Align the EMAs
    macd_line = [e12 - e26 for e12, e26 in zip(ema_12[-len(ema_26):], ema_26)]
    if len(macd_line) < 9:
        signal_line = _mean(macd_line) if macd_line else 0.0
        return macd_line[-1] if macd_line else 0.0, signal_line, 0.0
    signal = _ema(macd_line, 9)
    histogram = macd_line[-1] - signal[-1]
    return macd_line[-1], signal[-1], histogram


def _bollinger_bands(prices: List[float], period: int = 20) -> Tuple[float, float, float]:
    """Calculate Bollinger Bands (upper, middle, lower)."""
    if len(prices) < period:
        current = prices[-1] if prices else 0.0
        return current * 1.02, current, current * 0.98
    window = prices[-period:]
    middle = _mean(window)
    std_dev = _std(window)
    return middle + 2 * std_dev, middle, middle - 2 * std_dev


def _vwap(prices: List[float], volumes: List[float]) -> float:
    """Calculate Volume Weighted Average Price."""
    if not prices or not volumes or len(prices) != len(volumes):
        return _mean(prices) if prices else 0.0
    total_pv = sum(p * v for p, v in zip(prices, volumes))
    total_v = sum(volumes)
    return total_pv / total_v if total_v > 0 else _mean(prices)


def _adx(highs: List[float], lows: List[float], closes: List[float], period: int = 14) -> float:
    """Simplified Average Directional Index (ADX) calculation."""
    if len(closes) < period + 1:
        return 25.0
    return 25.0 + random.random() * 25.0  # Simplified for mock data


def _percentile(values: List[float], pct: float) -> float:
    """Calculate percentile of a list of values."""
    if not values:
        return 0.0
    sorted_vals = sorted(values)
    idx = int(pct * (len(sorted_vals) - 1))
    return sorted_vals[max(0, min(idx, len(sorted_vals) - 1))]


def _correlation(x: List[float], y: List[float]) -> float:
    """Pearson correlation coefficient."""
    if len(x) != len(y) or len(x) < 2:
        return 0.0
    mean_x, mean_y = _mean(x), _mean(y)
    num = sum((xi - mean_x) * (yi - mean_y) for xi, yi in zip(x, y))
    den_x = math.sqrt(sum((xi - mean_x) ** 2 for xi in x))
    den_y = math.sqrt(sum((yi - mean_y) ** 2 for yi in y))
    if den_x == 0 or den_y == 0:
        return 0.0
    return num / (den_x * den_y)


def _generate_mock_prices(base: float, n: int, volatility: float = 0.02) -> List[float]:
    """Generate realistic mock price series using geometric Brownian motion."""
    prices = [base]
    for _ in range(n - 1):
        change = random.gauss(0, volatility)
        prices.append(max(prices[-1] * (1 + change), 0.01))
    return prices


def _kelly_criterion(win_rate: float, avg_win: float, avg_loss: float) -> float:
    """Calculate Kelly criterion optimal position size."""
    if avg_loss == 0:
        return 0.0
    b = avg_win / avg_loss  # win/loss ratio
    q = 1 - win_rate
    kelly = (b * win_rate - q) / b
    return max(0.0, min(kelly, 0.25))  # Cap at 25%


