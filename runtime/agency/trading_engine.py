class TradingEngine:
    """
    Core trading engine for JARVIS BRAINIAC.

    Provides **analysis capabilities** and **trade execution interfaces**.
    No trade is executed without explicit user approval.

    Attributes:
        name          : Human-readable engine identifier.
        mode          : 'live' or 'paper'.
        max_position_pct : Max % of capital in a single position (default 20%).
        daily_loss_limit : Max % of capital that can be lost in one day.
    """

    def __init__(
        self,
        name: str = "JARVIS-TradingEngine",
        mode: str = "paper",
        max_position_pct: float = 0.20,
        daily_loss_limit: float = 0.05,
        risk_free_rate: float = 0.02,
    ):
        self.name = name
        self.mode = mode.lower().strip()
        self.max_position_pct = max_position_pct
        self.daily_loss_limit = daily_loss_limit
        self.risk_free_rate = risk_free_rate

        # In-memory storage (trade journal)
        self._trade_history: List[Dict[str, Any]] = []
        self._active_trades: Dict[str, Dict[str, Any]] = {}
        self._strategies: Dict[str, Dict[str, Any]] = {}
        self._user_approvals: Dict[str, bool] = {}

        logger.info("TradingEngine '%s' initialised in '%s' mode.", name, self.mode)

    # ------------------------------------------------------------------
    #  Internal helpers
    # ------------------------------------------------------------------

    def _record_trade(self, trade: Dict[str, Any]) -> None:
        """Persist a trade to the in-memory journal."""
        trade["recorded_at"] = _current_timestamp()
        self._trade_history.append(trade)
        if trade.get("trade_id"):
            self._active_trades[trade["trade_id"]] = trade
        logger.debug("Recorded trade %s", trade.get("trade_id", "N/A"))

    def _request_approval(self, order: Dict[str, Any]) -> bool:
        """
        Render an approval prompt and wait for explicit user consent.
        In an interactive environment this would block; here we log and
        stash the pending approval state for the caller to resolve.
        """
        trade_id = order.get("trade_id", _generate_id("TX"))
        quantity = order.get("quantity", 0)
        price = order.get("price", order.get("limit_price", 0.0))
        estimated_value = quantity * price

        prompt = APPROVAL_PROMPT_TEMPLATE.format(
            side=order.get("side", "BUY").upper(),
            quantity=quantity,
            symbol=order.get("symbol", "???"),
            order_type=order.get("order_type", "MARKET"),
            price=f"{price:,.4f}" if price else "MARKET",
            estimated_value=estimated_value,
            strategy=order.get("strategy", "manual"),
            reason=order.get("reason", "User-initiated trade."),
        )
        logger.info("Approval requested for trade %s", trade_id)
        # Store as pending — caller / external loop must confirm
        self._user_approvals[trade_id] = False
        order["approval_prompt"] = prompt
        order["trade_id"] = trade_id
        order["status"] = OrderStatus.PENDING_APPROVAL.value
        return False  # Not yet approved

    def _generate_price_history(
        self,
        symbol: str,
        periods: int = 252,
        start_price: float = 100.0,
        volatility: float = 0.02,
        drift: float = 0.0003,
        seed: Optional[int] = None,
    ) -> pd.DataFrame:
        """
        Generate synthetic OHLCV price history using geometric Brownian motion.
        Used for demonstration / testing when no external feed is available.
        """
        if seed is not None:
            np.random.seed(seed)
        dates = pd.date_range(end=datetime.now(timezone.utc), periods=periods, freq="B")
        returns = np.random.normal(drift, volatility, periods)
        prices = start_price * np.exp(np.cumsum(returns))
        # Build OHLCV around the close
        noise = volatility * prices * 0.3
        df = pd.DataFrame(
            {
                "date": dates,
                "open": prices + np.random.normal(0, noise * 0.5, periods),
                "high": prices + np.abs(np.random.normal(noise, noise * 0.3, periods)),
                "low": prices - np.abs(np.random.normal(noise, noise * 0.3, periods)),
                "close": prices,
                "volume": np.random.randint(1_000_000, 50_000_000, periods),
            }
        )
        df.set_index("date", inplace=True)
        return df

