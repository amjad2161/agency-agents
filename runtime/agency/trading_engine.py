def _generate_id(prefix: str = "TX") -> str:
    """Generate a unique trade / operation identifier."""
    return f"{prefix}-{uuid.uuid4().hex[:12].upper()}"


def _current_timestamp() -> str:
    """ISO-8601 timestamp."""
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _safe_json(obj: Any) -> Any:
    """Recursively convert NumPy / Pandas types to Python-native JSON."""
    if isinstance(obj, (np.integer, np.int64, np.int32)):
        return int(obj)
    if isinstance(obj, (np.floating, np.float64, np.float32)):
        return float(obj)
    if isinstance(obj, np.ndarray):
        return obj.tolist()
    if isinstance(obj, pd.Series):
        return obj.to_dict()
    if isinstance(obj, pd.DataFrame):
        return obj.to_dict(orient="records")
    if isinstance(obj, dict):
        return {k: _safe_json(v) for k, v in obj.items()}
    if isinstance(obj, list):
        return [_safe_json(v) for v in obj]
    if isinstance(obj, Enum):
        return obj.value
    return obj


def _compute_sma(series: pd.Series, window: int) -> pd.Series:
    """Simple Moving Average."""
    return series.rolling(window=window, min_periods=1).mean()


def _compute_ema(series: pd.Series, span: int) -> pd.Series:
    """Exponential Moving Average."""
    return series.ewm(span=span, adjust=False, min_periods=1).mean()


def _compute_rsi(series: pd.Series, period: int = 14) -> pd.Series:
    """Relative Strength Index (0-100)."""
    delta = series.diff()
    gain = delta.clip(lower=0)
    loss = -delta.clip(upper=0)
    avg_gain = gain.rolling(window=period, min_periods=1).mean()
    avg_loss = loss.rolling(window=period, min_periods=1).mean()
    rs = avg_gain / (avg_loss + 1e-12)
    return 100 - (100 / (1 + rs))


def _compute_macd(
    series: pd.Series, fast: int = 12, slow: int = 26, signal: int = 9
) -> Tuple[pd.Series, pd.Series, pd.Series]:
    """MACD line, Signal line, and Histogram."""
    ema_fast = _compute_ema(series, fast)
    ema_slow = _compute_ema(series, slow)
    macd_line = ema_fast - ema_slow
    signal_line = _compute_ema(macd_line, signal)
    histogram = macd_line - signal_line
    return macd_line, signal_line, histogram


def _compute_bollinger(
    series: pd.Series, window: int = 20, num_std: float = 2.0
) -> Tuple[pd.Series, pd.Series, pd.Series]:
    """Bollinger Bands — middle, upper, lower."""
    middle = _compute_sma(series, window)
    std = series.rolling(window=window, min_periods=1).std()
    upper = middle + num_std * std
    lower = middle - num_std * std
    return middle, upper, lower


def _compute_atr(high: pd.Series, low: pd.Series, close: pd.Series, period: int = 14) -> pd.Series:
    """Average True Range."""
    tr1 = high - low
    tr2 = (high - close.shift(1)).abs()
    tr3 = (low - close.shift(1)).abs()
    tr = pd.concat([tr1, tr2, tr3], axis=1).max(axis=1)
    return tr.rolling(window=period, min_periods=1).mean()


def _compute_sharpe(returns: pd.Series, risk_free: float = 0.02, periods: int = 252) -> float:
    """Annualised Sharpe ratio."""
    if returns.std() == 0:
        return 0.0
    return (returns.mean() - risk_free / periods) / (returns.std() + 1e-12) * np.sqrt(periods)


def _compute_max_drawdown(equity_curve: pd.Series) -> float:
    """Maximum drawdown as a negative fraction."""
    peak = equity_curve.cummax()
    drawdown = (equity_curve - peak) / (peak + 1e-12)
    return drawdown.min()


def _signal_from_score(score: float) -> Signal:
    """Map a numeric score to a Signal enum."""
    if score >= 1.5:
        return Signal.STRONG_BUY
    if score >= 0.5:
        return Signal.BUY
    if score <= -1.5:
        return Signal.STRONG_SELL
    if score <= -0.5:
        return Signal.SELL
    return Signal.HOLD


