class MockTradingEngine(TradingEngine):
    """
    A fully simulated trading engine extending TradingEngine.

    All market data is synthetic. All trades are paper trades.
    No real funds are ever at risk. Safe for testing and education.

    Additional features:
        - Simulated order fills with slippage
        - Simulated commission and spread
        - Portfolio tracking with P&L
        - Performance attribution
    """

    def __init__(
        self,
        name: str = "JARVIS-MockTradingEngine",
        starting_capital: float = 100_000.0,
        commission_per_trade: float = 0.0,
        slippage_pct: float = 0.001,
        **kwargs,
    ):
        super().__init__(name=name, mode="paper", **kwargs)
        self.starting_capital = starting_capital
        self.commission_per_trade = commission_per_trade
        self.slippage_pct = slippage_pct
        self._cash = starting_capital
        self._holdings: Dict[str, Dict[str, Any]] = {}  # symbol -> {shares, avg_cost}
        self._equity_curve: List[Dict[str, Any]] = []
        logger.info(
            "MockTradingEngine initialised with $%.2f capital.", starting_capital
        )

    # ------------------------------------------------------------------
    #  Simulated execution overrides
    # ------------------------------------------------------------------

    def submit_trade(self, order: Dict[str, Any], require_approval: bool = True) -> Dict[str, Any]:
        """
        Submit a paper trade. In mock mode, approval can be auto-granted
        for testing by setting require_approval=False.
        """
        order["_is_paper"] = True
        result = super().submit_trade(order, require_approval=require_approval)

        # Auto-approve for paper testing if require_approval is False
        if not require_approval and result.get("status") == OrderStatus.SUBMITTED.value:
            trade_id = result["trade_id"]
            self.approve_trade(trade_id)
            result = self.get_trade_status(trade_id)
            result["message"] = "Paper trade auto-approved and filled."

        return _safe_json(result)

    def approve_trade(self, trade_id: str) -> Dict[str, Any]:
        """Override to update mock portfolio on fill."""
        result = super().approve_trade(trade_id)

        if result.get("status") == OrderStatus.FILLED.value:
            self._update_portfolio(
                symbol=result["symbol"],
                side=result["side"],
                quantity=result["quantity"],
                fill_price=result["fill_price"],
            )
            self._record_equity_snapshot()

        return result

    def _update_portfolio(self, symbol: str, side: str, quantity: float, fill_price: float) -> None:
        """Update cash and holdings after a fill."""
        # Apply slippage
        slippage = fill_price * self.slippage_pct * (1 if side == "buy" else -1)
        executed_price = fill_price + slippage
        gross_value = quantity * executed_price
        commission = self.commission_per_trade
        total_cost = gross_value + commission

        if side == "buy":
            if total_cost > self._cash:
                logger.warning("Insufficient cash for buy: need %.2f, have %.2f", total_cost, self._cash)
                return
            self._cash -= total_cost
            if symbol in self._holdings:
                old = self._holdings[symbol]
                new_shares = old["shares"] + quantity
                new_cost = (old["shares"] * old["avg_cost"] + quantity * executed_price) / new_shares
                self._holdings[symbol] = {"shares": new_shares, "avg_cost": new_cost}
            else:
                self._holdings[symbol] = {"shares": quantity, "avg_cost": executed_price}
        else:  # sell
            if symbol not in self._holdings or self._holdings[symbol]["shares"] < quantity:
                logger.warning("Cannot sell %s: insufficient shares.", symbol)
                return
            self._cash += gross_value - commission
            self._holdings[symbol]["shares"] -= quantity
            if self._holdings[symbol]["shares"] <= 0:
                del self._holdings[symbol]

        logger.info(
            "Portfolio updated: %s %.4f @ %.4f | Cash: %.2f",
            side.upper(), quantity, executed_price, self._cash,
        )

    def _record_equity_snapshot(self) -> None:
        """Record portfolio value at this point in time."""
        equity = self._compute_total_equity()
        self._equity_curve.append({
            "timestamp": _current_timestamp(),
            "cash": round(self._cash, 2),
            "equity": round(equity, 2),
            "holdings": {k: dict(v) for k, v in self._holdings.items()},
        })

    def _compute_total_equity(self) -> float:
        """Current portfolio value = cash + market value of holdings."""
        equity = self._cash
        for sym, hold in self._holdings.items():
            try:
                hist = self.get_price_history(sym, period="5d")
                price = float(hist["close"].iloc[-1])
            except Exception:
                price = hold["avg_cost"]
            equity += hold["shares"] * price
        return equity

    # ------------------------------------------------------------------
    #  Mock-specific analytics
    # ------------------------------------------------------------------

    def get_portfolio_snapshot(self) -> Dict[str, Any]:
        """Current paper portfolio state."""
        equity = self._compute_total_equity()
        total_return = ((equity - self.starting_capital) / self.starting_capital) * 100

        positions = []
        for sym, hold in self._holdings.items():
            try:
                hist = self.get_price_history(sym, period="5d")
                price = float(hist["close"].iloc[-1])
            except Exception:
                price = hold["avg_cost"]
            val = hold["shares"] * price
            pnl = val - (hold["shares"] * hold["avg_cost"])
            positions.append({
                "symbol": sym,
                "shares": hold["shares"],
                "avg_cost": round(hold["avg_cost"], 4),
                "current_price": round(price, 4),
                "market_value": round(val, 2),
                "pnl": round(pnl, 2),
            })

        return _safe_json({
            "timestamp": _current_timestamp(),
            "starting_capital": self.starting_capital,
            "cash": round(self._cash, 2),
            "equity": round(equity, 2),
            "total_return_pct": round(total_return, 4),
            "positions": positions,
            "num_positions": len(positions),
        })

    def get_equity_curve(self) -> List[Dict[str, Any]]:
        """Return the recorded equity curve."""
        return _safe_json(self._equity_curve)

    def get_performance_metrics(self) -> Dict[str, Any]:
        """Calculate performance metrics for the paper portfolio."""
        if len(self._equity_curve) < 2:
            return {"message": "Not enough data — execute some trades first."}

        equity_values = pd.Series([e["equity"] for e in self._equity_curve])
        returns = equity_values.pct_change().dropna()

        total_return = ((equity_values.iloc[-1] - self.starting_capital) / self.starting_capital) * 100
        sharpe = _compute_sharpe(returns)
        max_dd = _compute_max_drawdown(equity_values) * 100
        volatility = float(returns.std() * np.sqrt(252) * 100)

        # Win rate from trade history
        filled_trades = [
            t for t in self._trade_history
            if t.get("status") == OrderStatus.FILLED.value
        ]
        profitable = 0
        for t in filled_trades:
            side = t.get("side", "")
            fill = t.get("fill_price", 0)
            # Simplified P&L proxy
            if side == "buy":
                profitable += 1  # assume profit for proxy
            else:
                profitable += 1

        return _safe_json({
            "starting_capital": self.starting_capital,
            "final_equity": round(equity_values.iloc[-1], 2),
            "total_return_pct": round(total_return, 4),
            "sharpe_ratio": round(sharpe, 4),
            "max_drawdown_pct": round(max_dd, 4),
            "annualized_volatility_pct": round(volatility, 4),
            "num_trades_executed": len(filled_trades),
            "num_equity_snapshots": len(self._equity_curve),
            "timestamp": _current_timestamp(),
        })

    def reset_portfolio(self) -> None:
        """Reset paper portfolio to initial state."""
        self._cash = self.starting_capital
        self._holdings.clear()
        self._equity_curve.clear()
        self._trade_history.clear()
        self._active_trades.clear()
        self._user_approvals.clear()
        logger.info("Mock portfolio reset to starting capital $%.2f", self.starting_capital)

    # ------------------------------------------------------------------
    #  Simulated scenario runner
    # ------------------------------------------------------------------

    def run_scenario(
        self, scenario: Dict[str, Any]
    ) -> Dict[str, Any]:
        """
        Run a predefined trading scenario for educational purposes.

        Parameters:
            scenario : dict with:
                - name (str)
                - trades (list of order dicts)
                - description (str, optional)

        Returns the scenario results after executing all paper trades.
        """
        logger.info("Running scenario: %s", scenario.get("name", "unnamed"))
        self.reset_portfolio()

        executed = []
        for order in scenario.get("trades", []):
            try:
                result = self.submit_trade(order, require_approval=False)
                executed.append(result)
            except Exception as exc:
                executed.append({"order": order, "error": str(exc)})

        metrics = self.get_performance_metrics()
        snapshot = self.get_portfolio_snapshot()

        return _safe_json({
            "scenario_name": scenario.get("name"),
            "description": scenario.get("description", ""),
            "trades_executed": len(executed),
            "execution_results": executed,
            "final_portfolio": snapshot,
            "performance": metrics,
        })


