    def calculate_position_size(
        self, capital: float, risk_percent: float, stop_loss: float
    ) -> float:
        """
        Calculate position size using the fixed-fractional risk model.

        Parameters:
            capital      : Total available capital.
            risk_percent : % of capital to risk on this trade (e.g. 0.02 = 2%).
            stop_loss    : Stop-loss distance as a fraction (e.g. 0.05 = 5%).

        Returns the number of shares / units to purchase.
        """
        if capital <= 0 or risk_percent <= 0 or stop_loss <= 0:
            raise TradingError("All inputs must be positive.")

        risk_amount = capital * risk_percent
        position_size = risk_amount / (stop_loss + 1e-12)

        # Enforce max position constraint
        max_position = capital * self.max_position_pct
        if position_size > max_position:
            logger.warning(
                "Position size %.2f exceeds max allowed %.2f; capping.",
                position_size, max_position,
            )
            position_size = max_position

        return round(position_size, 4)

    def calculate_var(
        self, portfolio: Dict[str, Dict[str, Any]], confidence: float = 0.95
    ) -> float:
        """
        Calculate parametric Value at Risk (VaR) for a portfolio.

        Parameters:
            portfolio : Dict mapping symbol -> {"value": float, "weight": float}
                        or {"shares": int, "price": float}.
            confidence : Confidence level (default 0.95 = 95%).

        Returns the portfolio VaR as a positive dollar amount.
        """
        if not portfolio:
            return 0.0

        total_value = 0.0
        weighted_vol = 0.0

        for sym, holding in portfolio.items():
            if "value" in holding:
                val = holding["value"]
            elif "shares" in holding and "price" in holding:
                val = holding["shares"] * holding["price"]
            else:
                continue

            total_value += val
            # Estimate annual volatility from price history
            try:
                hist = self.get_price_history(sym, period="1y")
                rets = np.log(hist["close"] / hist["close"].shift(1)).dropna()
                ann_vol = rets.std() * np.sqrt(252)
            except Exception:
                ann_vol = 0.25  # fallback 25%

            weight = val / (total_value + 1e-12)
            weighted_vol += weight * ann_vol

        # Parametric VaR
        z_score = abs(np.percentile(np.random.standard_normal(100_000), (1 - confidence) * 100))
        var = total_value * weighted_vol * z_score

        logger.info("Portfolio VaR (%.0f%%) = $%.2f", confidence * 100, var)
        return round(var, 2)

    def portfolio_analysis(self, holdings: Dict[str, Dict[str, Any]]) -> Dict[str, Any]:
        """
        Full portfolio analytics.

        Parameters:
            holdings : Dict mapping symbol -> {"shares": int, "avg_cost": float}

        Returns:
            - total_value, total_cost, total_pnl, total_pnl_pct
            - per-position breakdown (value, pnl, pnl_pct, weight)
            - sector_concentration (placeholder)
            - risk_metrics (volatility, VaR 95%, max_drawdown proxy)
        """
        logger.info("Running portfolio analysis on %d positions.", len(holdings))
        if not holdings:
            return {"error": "No holdings provided."}

        positions = []
        total_value = 0.0
        total_cost = 0.0

        for sym, hold in holdings.items():
            shares = hold.get("shares", 0)
            avg_cost = hold.get("avg_cost", 0.0)
            try:
                analysis = self.analyze_stock(sym)
                current_price = analysis["price_summary"]["current"]
            except Exception:
                current_price = avg_cost  # fallback

            value = shares * current_price
            cost = shares * avg_cost
            pnl = value - cost
            pnl_pct = (pnl / (cost + 1e-12)) * 100

            positions.append({
                "symbol": sym,
                "shares": shares,
                "avg_cost": round(avg_cost, 4),
                "current_price": round(current_price, 4),
                "value": round(value, 2),
                "cost": round(cost, 2),
                "pnl": round(pnl, 2),
                "pnl_pct": round(pnl_pct, 4),
            })
            total_value += value
            total_cost += cost

        total_pnl = total_value - total_cost
        total_pnl_pct = (total_pnl / (total_cost + 1e-12)) * 100

        # Weights
        for pos in positions:
            pos["weight_pct"] = round((pos["value"] / (total_value + 1e-12)) * 100, 2)

        # Portfolio-level risk — simulate a combined return series
        portfolio_returns = []
        for pos in positions:
            sym = pos["symbol"]
            try:
                hist = self.get_price_history(sym, period="1y")
                rets = hist["close"].pct_change().dropna()
                portfolio_returns.append(rets * (pos["weight_pct"] / 100))
            except Exception:
                continue

        if portfolio_returns:
            combined = pd.concat(portfolio_returns, axis=1).sum(axis=1)
            port_vol = float(combined.std() * np.sqrt(252) * 100)
            var_95 = self.calculate_var({p["symbol"]: {"value": p["value"]} for p in positions}, 0.95)
        else:
            port_vol = 0.0
            var_95 = 0.0

        # Concentration warning
        max_weight = max(p["weight_pct"] for p in positions) if positions else 0
        concentration_alert = max_weight > (self.max_position_pct * 100)

        result = {
            "timestamp": _current_timestamp(),
            "summary": {
                "total_value": round(total_value, 2),
                "total_cost": round(total_cost, 2),
                "total_pnl": round(total_pnl, 2),
                "total_pnl_pct": round(total_pnl_pct, 4),
                "num_positions": len(positions),
            },
            "positions": positions,
            "risk_metrics": {
                "annualized_volatility_pct": round(port_vol, 4),
                "value_at_risk_95": var_95,
                "max_position_weight_pct": round(max_weight, 2),
                "concentration_alert": concentration_alert,
                "daily_loss_limit_pct": round(self.daily_loss_limit * 100, 2),
            },
        }
        return _safe_json(result)

