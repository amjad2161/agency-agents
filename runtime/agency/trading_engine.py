# =============================================================================

def get_trading_engine(
    mode: str = "paper",
    starting_capital: float = 100_000.0,
    **kwargs,
) -> Union[TradingEngine, MockTradingEngine]:
    """
    Factory function to create the appropriate trading engine.

    Parameters:
        mode             : 'paper' (default) or 'mock' returns MockTradingEngine.
                           'live' returns TradingEngine (requires real broker config).
        starting_capital : Initial capital for mock/paper modes.
        **kwargs         : Passed through to the engine constructor.

    Returns:
        TradingEngine or MockTradingEngine instance.

    Usage:
        engine = get_trading_engine(mode="paper", starting_capital=50_000)
        analysis = engine.analyze_stock("AAPL")
        trade = engine.submit_trade({"symbol": "AAPL", "side": "buy", "quantity": 10})
        engine.approve_trade(trade["trade_id"])
    """
    mode_clean = mode.lower().strip()

    if mode_clean in ("paper", "mock"):
        logger.info("Creating MockTradingEngine (paper mode).")
        return MockTradingEngine(
            name=kwargs.get("name", "JARVIS-MockTradingEngine"),
            starting_capital=starting_capital,
            **kwargs,
        )

    if mode_clean == "live":
        logger.warning(
            "Creating LIVE TradingEngine. All trades require explicit approval. "
            "Ensure broker credentials and deposited funds are configured."
        )
        return TradingEngine(
            name=kwargs.get("name", "JARVIS-LiveTradingEngine"),
            mode="live",
            **kwargs,
        )

    logger.warning("Unknown mode '%s' — defaulting to paper.", mode)
    return MockTradingEngine(
        name=kwargs.get("name", "JARVIS-MockTradingEngine"),
        starting_capital=starting_capital,
        **kwargs,
    )


