    def analyze_stock(self, symbol: str) -> Dict[str, Any]:
        """
        Perform fundamental + technical analysis on a stock symbol.

        Returns a dict with:
            - symbol, asset_class
            - price_summary (current, change, change_pct)
            - technical_indicators (SMA, EMA, RSI, MACD, Bollinger, ATR)
            - signal (STRONG_BUY / BUY / HOLD / SELL / STRONG_SELL)
            - score (-2.0 to +2.0 composite)
            - analysis_notes (human-readable commentary)
        """
        logger.info("Analyzing stock: %s", symbol)
        data = self.get_price_history(symbol, period="6mo")
        if data.empty:
            raise TradingError(f"No price data available for {symbol}.")

        close = data["close"]
        high = data["high"]
        low = data["low"]
        volume = data.get("volume", pd.Series(index=close.index, data=0))

        # ---- Technical indicators ----
        sma20 = _compute_sma(close, INDICATOR_PERIODS["sma_short"])
        sma50 = _compute_sma(close, INDICATOR_PERIODS["sma_long"])
        ema12 = _compute_ema(close, INDICATOR_PERIODS["ema_fast"])
        ema26 = _compute_ema(close, INDICATOR_PERIODS["ema_slow"])
        rsi = _compute_rsi(close, INDICATOR_PERIODS["rsi"])
        macd_line, macd_signal, macd_hist = _compute_macd(close)
        bb_mid, bb_upper, bb_lower = _compute_bollinger(close)
        atr = _compute_atr(high, low, close)

        current_price = float(close.iloc[-1])
        prev_price = float(close.iloc[-2]) if len(close) > 1 else current_price
        change = current_price - prev_price
        change_pct = (change / (prev_price + 1e-12)) * 100

        # ---- Composite scoring (-2 to +2) ----
        score = 0.0
        notes: List[str] = []

        # Trend — SMA crossover
        if sma20.iloc[-1] > sma50.iloc[-1]:
            score += 0.5
            notes.append("Short-term SMA above long-term SMA (bullish trend).")
        else:
            score -= 0.5
            notes.append("Short-term SMA below long-term SMA (bearish trend).")

        # RSI
        current_rsi = float(rsi.iloc[-1])
        if current_rsi < 30:
            score += 0.6
            notes.append(f"RSI oversold ({current_rsi:.1f}) — potential reversal up.")
        elif current_rsi > 70:
            score -= 0.6
            notes.append(f"RSI overbought ({current_rsi:.1f}) — potential reversal down.")
        else:
            notes.append(f"RSI neutral ({current_rsi:.1f}).")

        # MACD
        if macd_hist.iloc[-1] > 0 and macd_hist.iloc[-2] <= 0:
            score += 0.5
            notes.append("MACD histogram turned positive (bullish crossover).")
        elif macd_hist.iloc[-1] < 0 and macd_hist.iloc[-2] >= 0:
            score -= 0.5
            notes.append("MACD histogram turned negative (bearish crossover).")
        else:
            notes.append("MACD histogram continuing current trend.")

        # Bollinger position
        if current_price <= bb_lower.iloc[-1]:
            score += 0.4
            notes.append("Price at lower Bollinger Band — potential bounce.")
        elif current_price >= bb_upper.iloc[-1]:
            score -= 0.4
            notes.append("Price at upper Bollinger Band — potential pullback.")
        else:
            notes.append("Price within Bollinger Bands — normal range.")

        # Volume confirmation
        avg_vol = volume.rolling(20, min_periods=1).mean().iloc[-1]
        if volume.iloc[-1] > avg_vol * 1.5:
            notes.append("Volume spike detected — confirming move strength.")
            score += np.sign(score) * 0.2 if score != 0 else 0.0

        # Clamp score and derive signal
        score = max(-2.0, min(2.0, score))
        signal = _signal_from_score(score)

        result = {
            "symbol": symbol.upper(),
            "asset_class": AssetClass.EQUITY.value,
            "timestamp": _current_timestamp(),
            "price_summary": {
                "current": round(current_price, 4),
                "change": round(change, 4),
                "change_pct": round(change_pct, 4),
                "day_high": float(high.iloc[-1]),
                "day_low": float(low.iloc[-1]),
                "volume": int(volume.iloc[-1]),
            },
            "technical_indicators": {
                "sma20": round(float(sma20.iloc[-1]), 4),
                "sma50": round(float(sma50.iloc[-1]), 4),
                "ema12": round(float(ema12.iloc[-1]), 4),
                "ema26": round(float(ema26.iloc[-1]), 4),
                "rsi14": round(current_rsi, 2),
                "macd_line": round(float(macd_line.iloc[-1]), 6),
                "macd_signal": round(float(macd_signal.iloc[-1]), 6),
                "macd_histogram": round(float(macd_hist.iloc[-1]), 6),
                "bb_middle": round(float(bb_mid.iloc[-1]), 4),
                "bb_upper": round(float(bb_upper.iloc[-1]), 4),
                "bb_lower": round(float(bb_lower.iloc[-1]), 4),
                "atr14": round(float(atr.iloc[-1]), 4),
            },
            "composite_score": round(score, 4),
            "signal": signal.name,
            "analysis_notes": notes,
        }
        return _safe_json(result)

    def analyze_crypto(self, symbol: str) -> Dict[str, Any]:
        """
        Crypto-specific market analysis.

        Extends stock analysis with crypto-specific metrics:
            - 24h volume dominance estimate
            - volatility regime (annualised)
            - network sentiment proxy
        """
        logger.info("Analyzing crypto: %s", symbol)
        # Crypto is typically more volatile — increase synthetic volatility
        data = self.get_price_history(symbol, period="3mo", volatility_override=0.045)
        if data.empty:
            raise TradingError(f"No price data for crypto {symbol}.")

        close = data["close"]
        high = data["high"]
        low = data["low"]
        volume = data.get("volume", pd.Series(index=close.index, data=0))

        # Standard indicators
        sma20 = _compute_sma(close, 20)
        sma50 = _compute_sma(close, 50)
        rsi = _compute_rsi(close, 14)
        macd_line, macd_signal, macd_hist = _compute_macd(close)
        bb_mid, bb_upper, bb_lower = _compute_bollinger(close, 20, 2.5)
        atr = _compute_atr(high, low, close, 14)

        current_price = float(close.iloc[-1])
        change_pct = ((current_price - float(close.iloc[-2])) / float(close.iloc[-2])) * 100

        # Crypto-specific metrics
        log_returns = np.log(close / close.shift(1)).dropna()
        annual_vol = float(log_returns.std() * np.sqrt(365) * 100)

        # Volatility regime
        vol_regime = "low"
        if annual_vol > 80:
            vol_regime = "extreme"
        elif annual_vol > 50:
            vol_regime = "high"
        elif annual_vol > 30:
            vol_regime = "moderate"

        # Composite score (crypto-weighted)
        score = 0.0
        notes: List[str] = []

        if sma20.iloc[-1] > sma50.iloc[-1]:
            score += 0.4
            notes.append("SMA bullish alignment.")
        else:
            score -= 0.4
            notes.append("SMA bearish alignment.")

        rsi_val = float(rsi.iloc[-1])
        if rsi_val < 30:
            score += 0.7
            notes.append(f"Deep oversold RSI ({rsi_val:.1f}) — possible relief rally.")
        elif rsi_val > 75:
            score -= 0.7
            notes.append(f"Extreme overbought RSI ({rsi_val:.1f}) — correction risk.")
        else:
            notes.append(f"RSI at {rsi_val:.1f}.")

        if macd_hist.iloc[-1] > 0:
            score += 0.3
            notes.append("MACD positive.")
        else:
            score -= 0.3
            notes.append("MACD negative.")

        # Volatility regime signal adjustment
        if vol_regime in ("extreme", "high") and score > 0.5:
            score -= 0.3
            notes.append("High volatility regime — caution warranted on longs.")
        elif vol_regime in ("extreme", "high") and score < -0.5:
            score += 0.2
            notes.append("High volatility may indicate exhaustion — watch for reversal.")

        score = max(-2.0, min(2.0, score))
        signal = _signal_from_score(score)

        result = {
            "symbol": symbol.upper(),
            "asset_class": AssetClass.CRYPTO.value,
            "timestamp": _current_timestamp(),
            "price_summary": {
                "current": round(current_price, 6),
                "change_pct": round(change_pct, 4),
                "day_high": float(high.iloc[-1]),
                "day_low": float(low.iloc[-1]),
                "volume": int(volume.iloc[-1]),
            },
            "technical_indicators": {
                "sma20": round(float(sma20.iloc[-1]), 4),
                "sma50": round(float(sma50.iloc[-1]), 4),
                "rsi14": round(rsi_val, 2),
                "macd": round(float(macd_line.iloc[-1]), 6),
                "macd_signal": round(float(macd_signal.iloc[-1]), 6),
                "atr14": round(float(atr.iloc[-1]), 6),
                "bb_upper": round(float(bb_upper.iloc[-1]), 6),
                "bb_lower": round(float(bb_lower.iloc[-1]), 6),
            },
            "crypto_specific": {
                "annualized_volatility_pct": round(annual_vol, 2),
                "volatility_regime": vol_regime,
                "avg_daily_volume": int(volume.mean()),
            },
            "composite_score": round(score, 4),
            "signal": signal.name,
            "analysis_notes": notes,
        }
        return _safe_json(result)

