    def submit_trade(self, order: Dict[str, Any], require_approval: bool = True) -> Dict[str, Any]:
        """
        Submit a trade order. By default this REQUIRES explicit user approval.

        Parameters:
            order : dict with keys:
                - symbol (str, required)
                - side   ('buy' or 'sell', required)
                - quantity (int/float, required)
                - order_type ('market', 'limit', 'stop', default 'market')
                - price / limit_price (float, optional)
                - stop_price (float, for stop orders)
                - strategy (str, optional — strategy that generated this trade)
                - reason (str, optional — human-readable rationale)
            require_approval : If True, the trade is held in PENDING_APPROVAL
                               until explicitly approved.

        Returns:
            dict with trade_id, status, and (if pending) the approval prompt.
        """
        # ---- Validate required fields ----
        symbol = order.get("symbol", "").upper()
        side = order.get("side", "").lower()
        quantity = order.get("quantity", 0)
        order_type = order.get("order_type", "market").lower()

        if not symbol:
            raise InvalidOrderError("'symbol' is required.")
        if side not in ("buy", "sell"):
            raise InvalidOrderError("'side' must be 'buy' or 'sell'.")
        if quantity <= 0:
            raise InvalidOrderError("'quantity' must be positive.")

        trade_id = _generate_id("TX")
        order["trade_id"] = trade_id
        order["symbol"] = symbol
        order["side"] = side
        order["order_type"] = order_type
        order["submitted_at"] = _current_timestamp()

        # Safety: paper mode guard
        if self.mode == "paper":
            order["mode"] = "paper"
            logger.info("Trade %s running in PAPER mode — no real funds at risk.", trade_id)

        if require_approval:
            self._request_approval(order)
            self._record_trade(order)
            return _safe_json({
                "trade_id": trade_id,
                "status": OrderStatus.PENDING_APPROVAL.value,
                "message": "Trade pending explicit user approval.",
                "approval_prompt": order.get("approval_prompt", ""),
            })

        # Direct execution (still requires mode != 'live' unless explicitly configured)
        order["status"] = OrderStatus.SUBMITTED.value
        self._record_trade(order)
        return _safe_json({
            "trade_id": trade_id,
            "status": OrderStatus.SUBMITTED.value,
            "message": "Trade submitted directly (approval bypassed — use with caution).",
        })

    def approve_trade(self, trade_id: str) -> Dict[str, Any]:
        """
        Explicitly approve a pending trade.
        This is the REQUIRED step after submit_trade() when require_approval=True.
        """
        if trade_id not in self._active_trades:
            raise TradingError(f"Trade {trade_id} not found.")

        trade = self._active_trades[trade_id]
        if trade.get("status") != OrderStatus.PENDING_APPROVAL.value:
            return _safe_json({
                "trade_id": trade_id,
                "status": trade.get("status"),
                "message": "Trade is not in pending approval state.",
            })

        trade["status"] = OrderStatus.APPROVED.value
        trade["approved_at"] = _current_timestamp()
        self._user_approvals[trade_id] = True

        # Simulate fill
        trade["status"] = OrderStatus.FILLED.value
        trade["filled_at"] = _current_timestamp()
        trade["fill_price"] = trade.get("price", trade.get("limit_price", 0.0))
        if trade["fill_price"] == 0.0:
            # Pull latest price
            try:
                hist = self.get_price_history(trade["symbol"], period="5d")
                trade["fill_price"] = float(hist["close"].iloc[-1])
            except Exception:
                trade["fill_price"] = 0.0

        logger.info("Trade %s APPROVED and FILLED at %.4f", trade_id, trade["fill_price"])
        return _safe_json({
            "trade_id": trade_id,
            "status": OrderStatus.FILLED.value,
            "symbol": trade["symbol"],
            "side": trade["side"],
            "quantity": trade["quantity"],
            "fill_price": trade["fill_price"],
            "filled_at": trade["filled_at"],
            "message": "Trade approved and filled.",
        })

    def reject_trade(self, trade_id: str) -> Dict[str, Any]:
        """Explicitly reject a pending trade."""
        if trade_id not in self._active_trades:
            raise TradingError(f"Trade {trade_id} not found.")

        trade = self._active_trades[trade_id]
        trade["status"] = OrderStatus.REJECTED.value
        trade["rejected_at"] = _current_timestamp()
        self._user_approvals[trade_id] = False

        logger.info("Trade %s REJECTED by user.", trade_id)
        return _safe_json({
            "trade_id": trade_id,
            "status": OrderStatus.REJECTED.value,
            "message": "Trade rejected by user.",
        })

    def get_trade_status(self, trade_id: str) -> Dict[str, Any]:
        """Retrieve the current status of a trade."""
        trade = self._active_trades.get(trade_id)
        if not trade:
            return {"trade_id": trade_id, "status": "not_found", "message": "Trade not found."}
        return _safe_json({
            "trade_id": trade_id,
            "status": trade.get("status"),
            "symbol": trade.get("symbol"),
            "side": trade.get("side"),
            "quantity": trade.get("quantity"),
            "submitted_at": trade.get("submitted_at"),
            "filled_at": trade.get("filled_at"),
            "fill_price": trade.get("fill_price"),
        })

    def cancel_trade(self, trade_id: str) -> bool:
        """
        Cancel a trade that has not yet been filled.
        Returns True if cancellation succeeded.
        """
        trade = self._active_trades.get(trade_id)
        if not trade:
            logger.warning("Cancel failed: trade %s not found.", trade_id)
            return False

        if trade.get("status") in (
            OrderStatus.FILLED.value,
            OrderStatus.CANCELLED.value,
            OrderStatus.REJECTED.value,
        ):
            logger.warning("Cancel failed: trade %s is already %s.", trade_id, trade["status"])
            return False

        trade["status"] = OrderStatus.CANCELLED.value
        trade["cancelled_at"] = _current_timestamp()
        logger.info("Trade %s CANCELLED.", trade_id)
        return True

    def get_trade_history(
        self,
        symbol: Optional[str] = None,
        status: Optional[str] = None,
        limit: int = 100,
    ) -> List[Dict[str, Any]]:
        """
        Retrieve the trade journal with optional filters.

        Parameters:
            symbol : Filter by ticker.
            status : Filter by OrderStatus value.
            limit  : Max records to return (most recent first).
        """
        filtered = list(self._trade_history)
        if symbol:
            filtered = [t for t in filtered if t.get("symbol") == symbol.upper()]
        if status:
            filtered = [t for t in filtered if t.get("status") == status.lower()]

        filtered.sort(key=lambda x: x.get("submitted_at", ""), reverse=True)
        return _safe_json(filtered[:limit])

    # =================================================================
    #  Reporting
    # =================================================================

    def generate_trade_report(self, start_date: Optional[str] = None, end_date: Optional[str] = None) -> Dict[str, Any]:
        """
        Generate a summary report of all trading activity.
        """
        trades = self._trade_history
        if start_date:
            trades = [t for t in trades if t.get("submitted_at", "") >= start_date]
        if end_date:
            trades = [t for t in trades if t.get("submitted_at", "") <= end_date]

        total_trades = len(trades)
        filled = [t for t in trades if t.get("status") == OrderStatus.FILLED.value]
        rejected = [t for t in trades if t.get("status") == OrderStatus.REJECTED.value]
        cancelled = [t for t in trades if t.get("status") == OrderStatus.CANCELLED.value]
        pending = [t for t in trades if t.get("status") == OrderStatus.PENDING_APPROVAL.value]

        buy_trades = [t for t in filled if t.get("side") == "buy"]
        sell_trades = [t for t in filled if t.get("side") == "sell"]

        return _safe_json({
            "period": {"start": start_date, "end": end_date},
            "summary": {
                "total_trades_submitted": total_trades,
                "filled": len(filled),
                "rejected": len(rejected),
                "cancelled": len(cancelled),
                "pending_approval": len(pending),
                "buy_orders": len(buy_trades),
                "sell_orders": len(sell_trades),
            },
            "engine_info": {
                "name": self.name,
                "mode": self.mode,
                "max_position_pct": self.max_position_pct,
                "daily_loss_limit": self.daily_loss_limit,
            },
            "timestamp": _current_timestamp(),
        })


