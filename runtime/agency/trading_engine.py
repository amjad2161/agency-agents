    def create_strategy(self, name: str, rules: List[Dict[str, Any]]) -> Dict[str, Any]:
        """
        Define a named trading strategy from a list of entry / exit rules.

        Each rule is a dict:
            {
                "type": "entry" | "exit",
                "indicator": "sma_crossover" | "rsi" | "macd" | "bollinger" | "custom",
                "params": { ... },
                "logic": "AND" | "OR",
            }

        Returns the registered strategy dict with its unique ID.
        """
        logger.info("Creating strategy '%s' with %d rules.", name, len(rules))
        strategy_id = _generate_id("STGY")
        strategy = {
            "strategy_id": strategy_id,
            "name": name,
            "rules": rules,
            "created_at": _current_timestamp(),
            "backtest_results": None,
            "optimized": False,
        }
        self._strategies[strategy_id] = strategy
        return _safe_json(strategy)

    def backtest_strategy(self, strategy: Dict[str, Any], data: pd.DataFrame) -> Dict[str, Any]:
        """
        Backtest a strategy against historical price data.

        Parameters:
            strategy : Strategy dict (from create_strategy).
            data     : OHLCV DataFrame with at least 'close' column.

        Returns comprehensive backtest metrics:
            - total_return, annualized_return
            - sharpe_ratio, max_drawdown
            - win_rate, num_trades, avg_trade_return
            - equity_curve (list of daily values)
        """
        logger.info("Backtesting strategy '%s'.", strategy.get("name", "unknown"))
        if data.empty or "close" not in data.columns:
            raise StrategyError("Backtest data must contain a 'close' column.")

        close = data["close"].copy()
        returns = close.pct_change().fillna(0)

        # Simple rule-based signal generation
        signals = pd.Series(0, index=close.index)  # 0 = hold, 1 = long, -1 = short

        for rule in strategy.get("rules", []):
            ind = rule.get("indicator", "")
            params = rule.get("params", {})

            if ind == "sma_crossover":
                short_w = params.get("short", 20)
                long_w = params.get("long", 50)
                sma_s = _compute_sma(close, short_w)
                sma_l = _compute_sma(close, long_w)
                mask = sma_s > sma_l
                signals[mask] = 1
                signals[~mask] = -1

            elif ind == "rsi":
                period = params.get("period", 14)
                oversold = params.get("oversold", 30)
                overbought = params.get("overbought", 70)
                rsi_vals = _compute_rsi(close, period)
                signals[rsi_vals < oversold] = 1
                signals[rsi_vals > overbought] = -1

            elif ind == "macd":
                macd_line, macd_signal, macd_hist = _compute_macd(close)
                signals[macd_hist > 0] = 1
                signals[macd_hist <= 0] = -1

            elif ind == "bollinger":
                bb_m, bb_u, bb_l = _compute_bollinger(close, params.get("window", 20))
                signals[close < bb_l] = 1
                signals[close > bb_u] = -1

            elif ind == "custom":
                # Allow custom signal override via params
                custom_signals = params.get("signals")
                if custom_signals is not None and len(custom_signals) == len(signals):
                    signals = pd.Series(custom_signals, index=close.index)

        # Shift signals to avoid lookahead bias
        position = signals.shift(1).fillna(0)
        strategy_returns = returns * position
        equity_curve = (1 + strategy_returns).cumprod()
        total_return = float(equity_curve.iloc[-1] - 1) if len(equity_curve) > 0 else 0.0

        # Trade-level statistics
        trades = []
        in_trade = False
        entry_price = 0.0
        for i, (date, ret) in enumerate(strategy_returns.items()):
            pos = position.iloc[i]
            price = close.iloc[i]
            if pos != 0 and not in_trade:
                in_trade = True
                entry_price = price
            elif pos == 0 and in_trade:
                in_trade = False
                pnl = (price - entry_price) / (entry_price + 1e-12)
                trades.append(pnl)
                entry_price = 0.0

        winning_trades = [t for t in trades if t > 0]
        win_rate = (len(winning_trades) / len(trades)) if trades else 0.0
        avg_trade = float(np.mean(trades)) if trades else 0.0

        num_years = max(len(returns) / 252, 0.01)
        annualized = (1 + total_return) ** (1 / num_years) - 1

        result = {
            "strategy_id": strategy.get("strategy_id"),
            "strategy_name": strategy.get("name"),
            "backtest_period": f"{len(data)} days",
            "total_return_pct": round(total_return * 100, 4),
            "annualized_return_pct": round(annualized * 100, 4),
            "sharpe_ratio": round(_compute_sharpe(strategy_returns), 4),
            "max_drawdown_pct": round(_compute_max_drawdown(equity_curve) * 100, 4),
            "win_rate_pct": round(win_rate * 100, 2),
            "num_trades": len(trades),
            "avg_trade_return_pct": round(avg_trade * 100, 4),
            "equity_curve": equity_curve.tolist(),
        }

        # Persist result on strategy
        if strategy.get("strategy_id") in self._strategies:
            self._strategies[strategy["strategy_id"]]["backtest_results"] = result

        return _safe_json(result)

    def optimize_strategy(
        self, strategy: Dict[str, Any], param_grid: Dict[str, List[Any]]
    ) -> Dict[str, Any]:
        """
        Grid-search parameter optimisation for a strategy.

        Parameters:
            strategy   : Strategy dict.
            param_grid : Dict of param_name -> list of values to try.
                         E.g. {"short": [10, 20, 30], "long": [40, 50, 60]}

        Returns the best parameter set and its backtest metrics.
        """
        logger.info("Optimizing strategy '%s' over grid: %s", strategy.get("name"), param_grid)
        data = self.get_price_history("SPY", period="2y")  # benchmark for optimisation

        best_sharpe = -999.0
        best_params = {}
        best_result = {}
        all_results = []

        keys = list(param_grid.keys())
        values = list(param_grid.values())

        import itertools

        for combo in itertools.product(*values):
            params = dict(zip(keys, combo))
            # Build a temporary rule set
            temp_rules = []
            for rule in strategy.get("rules", []):
                new_rule = {**rule, "params": {**rule.get("params", {}), **params}}
                temp_rules.append(new_rule)
            temp_strategy = {**strategy, "rules": temp_rules}

            try:
                result = self.backtest_strategy(temp_strategy, data)
                sharpe = result.get("sharpe_ratio", -999.0)
                all_results.append({"params": params, "sharpe": sharpe, "total_return": result.get("total_return_pct")})
                if sharpe > best_sharpe:
                    best_sharpe = sharpe
                    best_params = params
                    best_result = result
            except Exception as exc:
                logger.debug("Combo %s failed: %s", params, exc)

        optimization_report = {
            "strategy_id": strategy.get("strategy_id"),
            "strategy_name": strategy.get("name"),
            "best_parameters": best_params,
            "best_sharpe_ratio": round(best_sharpe, 4),
            "best_backtest": best_result,
            "grid_size": len(all_results),
            "all_results": sorted(all_results, key=lambda x: x["sharpe"], reverse=True)[:10],
            "timestamp": _current_timestamp(),
        }

        # Mark strategy as optimized
        if strategy.get("strategy_id") in self._strategies:
            self._strategies[strategy["strategy_id"]]["optimized"] = True
            self._strategies[strategy["strategy_id"]]["best_params"] = best_params

        return _safe_json(optimization_report)

    def compare_strategies(self, strategies: List[Dict[str, Any]]) -> Dict[str, Any]:
        """
        Compare multiple strategies on the same historical window.

        Returns a ranked comparison table plus a composite recommendation.
        """
        logger.info("Comparing %d strategies.", len(strategies))
        data = self.get_price_history("SPY", period="2y")
        comparison = []

        for stgy in strategies:
            try:
                result = self.backtest_strategy(stgy, data)
                comparison.append({
                    "strategy_id": stgy.get("strategy_id"),
                    "name": stgy.get("name"),
                    "total_return_pct": result.get("total_return_pct"),
                    "sharpe_ratio": result.get("sharpe_ratio"),
                    "max_drawdown_pct": result.get("max_drawdown_pct"),
                    "win_rate_pct": result.get("win_rate_pct"),
                    "num_trades": result.get("num_trades"),
                })
            except Exception as exc:
                logger.warning("Comparison failed for %s: %s", stgy.get("name"), exc)
                comparison.append({
                    "strategy_id": stgy.get("strategy_id"),
                    "name": stgy.get("name"),
                    "error": str(exc),
                })

        # Rank by Sharpe ratio
        valid = [c for c in comparison if "sharpe_ratio" in c]
        ranked = sorted(valid, key=lambda x: x["sharpe_ratio"], reverse=True)

        recommendation = "No valid strategies to compare."
        if ranked:
            best = ranked[0]
            recommendation = (
                f"Best strategy: '{best['name']}' with Sharpe {best['sharpe_ratio']:.2f} "
                f"and return {best['total_return_pct']:.2f}%."
            )

        return _safe_json({
            "ranked_strategies": ranked,
            "recommendation": recommendation,
            "benchmark": "SPY (2-year period)",
            "timestamp": _current_timestamp(),
        })

