    def screen_stocks(self, criteria: Dict[str, Any]) -> List[Dict[str, Any]]:
        """
        Screen a universe of stocks based on user criteria.

        Parameters:
            criteria: dict with optional keys:
                - min_price, max_price
                - min_volume
                - min_rsi, max_rsi
                - signal (one of Signal names)
                - max_volatility
                - symbols (list to restrict universe)

        Returns a list of dicts matching the criteria, sorted by composite_score.
        """
        logger.info("Screening stocks with criteria: %s", criteria)
        universe = criteria.get("symbols", ["AAPL", "MSFT", "GOOGL", "AMZN", "TSLA",
                                              "NVDA", "META", "NFLX", "AMD", "CRM",
                                              "PYPL", "INTC", "DIS", "BA", "V"])
        results = []
        for sym in universe:
            try:
                analysis = self.analyze_stock(sym)
                ps = analysis.get("price_summary", {})
                tech = analysis.get("technical_indicators", {})

                # Filters
                if criteria.get("min_price") and ps.get("current", 0) < criteria["min_price"]:
                    continue
                if criteria.get("max_price") and ps.get("current", 0) > criteria["max_price"]:
                    continue
                if criteria.get("min_volume") and ps.get("volume", 0) < criteria["min_volume"]:
                    continue
                if criteria.get("min_rsi") and tech.get("rsi14", 50) < criteria["min_rsi"]:
                    continue
                if criteria.get("max_rsi") and tech.get("rsi14", 50) > criteria["max_rsi"]:
                    continue
                if criteria.get("signal") and analysis.get("signal") != criteria["signal"]:
                    continue

                results.append({
                    "symbol": sym,
                    "price": ps.get("current"),
                    "change_pct": ps.get("change_pct"),
                    "rsi14": tech.get("rsi14"),
                    "composite_score": analysis.get("composite_score"),
                    "signal": analysis.get("signal"),
                })
            except Exception as exc:
                logger.warning("Screening failed for %s: %s", sym, exc)

        results.sort(key=lambda x: x["composite_score"], reverse=True)
        return _safe_json(results)

    def get_price_history(
        self, symbol: str, period: str = "1y", volatility_override: Optional[float] = None
    ) -> pd.DataFrame:
        """
        Retrieve historical OHLCV price data.

        In production this would query a market-data provider (Yahoo Finance,
        Alpha Vantage, etc.). For now we generate realistic synthetic data.

        Parameters:
            symbol   : Ticker or trading pair.
            period   : '1mo', '3mo', '6mo', '1y', '2y', '5y'.
            volatility_override : Optional custom volatility for synthetic data.

        Returns a pandas DataFrame with columns [open, high, low, close, volume].
        """
        period_map = {
            "1mo": 22,
            "3mo": 66,
            "6mo": 126,
            "1y": 252,
            "2y": 504,
            "5y": 1260,
        }
        periods = period_map.get(period, 252)
        vol = volatility_override or 0.02

        # Deterministic seed per symbol for consistency
        seed = int(hashlib.sha256(symbol.upper().encode()).hexdigest(), 16) % (2**31)
        start_price = max(10.0, (seed % 500) + 50.0)

        df = self._generate_price_history(
            symbol=symbol,
            periods=periods,
            start_price=start_price,
            volatility=vol,
            drift=0.0002,
            seed=seed,
        )
        return df

