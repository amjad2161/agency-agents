@dataclass
class _StreamBuffer:
    """Thread-safe accumulator for callbacks fired by the OOTB loop."""

    tool_outputs: List[Dict[str, Any]] = field(default_factory=list)
    llm_outputs: List[str] = field(default_factory=list)
    last_screenshot_b64: Optional[str] = None
    _lock: threading.Lock = field(default_factory=threading.Lock)

    def on_tool_output(self, result: Any, tool_id: str) -> None:
        """Called by OOTB whenever a tool finishes."""
        entry: Dict[str, Any] = {"tool_id": tool_id, "timestamp": _now_iso()}
        # result is expected to be a ToolResult-like object
        if hasattr(result, "output"):
            entry["output"] = result.output
        if hasattr(result, "error"):
            entry["error"] = result.error
        if hasattr(result, "base64_image") and result.base64_image:
            entry["has_image"] = True
            self.last_screenshot_b64 = result.base64_image
        if hasattr(result, "system"):
            entry["system"] = result.system
        with self._lock:
            self.tool_outputs.append(entry)

    def on_llm_output(self, block: Any) -> None:
        """Called by OOTB whenever the LLM emits a content block."""
        text = ""
        if hasattr(block, "text"):
            text = block.text
        elif isinstance(block, dict):
            text = block.get("text", "")
        with self._lock:
            if text:
                self.llm_outputs.append(text)

    def on_api_response(self, response: Any) -> None:
        """Called by OOTB after each API round-trip (optional telemetry)."""
        # Intentionally no-op for now; could record token usage here.
        pass


def _now_iso() -> str:
    from datetime import datetime, timezone

    return datetime.now(timezone.utc).isoformat()


def _resolve_ootb_path(extra_search_paths: Optional[List[str]] = None) -> Optional[Path]:
    """Locate the OOTB repository on disk.

    Resolution order:
    1. ``COMPUTER_USE_OOTB_ROOT`` environment variable.
    2. ``extra_search_paths`` (caller-provided list).
    3. Common relative locations from this bridge file.
    4. Python path scanning for ``computer_use_demo``.
    """
    env_root = os.environ.get("COMPUTER_USE_OOTB_ROOT")
    if env_root:
        p = Path(env_root).expanduser().resolve()
        if (p / "computer_use_demo" / "loop.py").exists():
            return p

    if extra_search_paths:
        for sp in extra_search_paths:
            p = Path(sp).expanduser().resolve()
            if (p / "computer_use_demo" / "loop.py").exists():
                return p

    # Relative to this bridge file: ../../../../../external_repos/computer_use_ootb
    bridge_dir = Path(__file__).resolve().parent
    candidates = [
        bridge_dir / "computer_use_ootb",
        bridge_dir.parent / "computer_use_ootb",
        bridge_dir.parent.parent / "external_repos" / "computer_use_ootb",
        bridge_dir.parent.parent.parent / "external_repos" / "computer_use_ootb",
    ]
    for c in candidates:
        if (c / "computer_use_demo" / "loop.py").exists():
            return c

    # Fallback: see if already importable
    try:
        import computer_use_demo

        return Path(computer_use_demo.__file__).resolve().parent.parent
    except Exception:
        pass

    return None


