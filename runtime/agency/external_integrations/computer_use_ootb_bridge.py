@dataclass
class OotbTaskResult:
    """Structured result returned by ``ComputerUseOotbBridge.execute``.

    Attributes
    ----------
    success : bool
        True if the loop terminated without an unhandled exception.
    message_history : list[dict]
        Complete message transcript (system, user, assistant, tool) in
        Anthropic BetaMessageParam-compatible dict form.
    tool_outputs : list[dict]
        Chronological list of every tool invocation and its result.
    llm_outputs : list[str]
        Text generated by the planner / actor at each turn.
    final_screenshot_b64 : str | None
        Base64-encoded PNG of the last screenshot captured, if any.
    error : str | None
        Human-readable error description when ``success`` is False.
    metadata : dict
        Arbitrary key/value pairs for observability (timings, token counts, etc.).
    """

    success: bool = False
    message_history: List[Dict[str, Any]] = field(default_factory=list)
    tool_outputs: List[Dict[str, Any]] = field(default_factory=list)
    llm_outputs: List[str] = field(default_factory=list)
    final_screenshot_b64: Optional[str] = None
    error: Optional[str] = None
    metadata: Dict[str, Any] = field(default_factory=dict)


