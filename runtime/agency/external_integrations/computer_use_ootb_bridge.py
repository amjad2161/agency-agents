def create_bridge_from_env(
    planner_model: PlannerModel | str = PlannerModel.GPT_4O,
    actor_model: ActorModel | str = ActorModel.SHOWUI,
    **override_kwargs: Any,
) -> ComputerUseOotbBridge:
    """Create a bridge reading all API keys from environment variables.

    Expected env vars::

        OPENAI_API_KEY      # required for GPT-4o planner
        ANTHROPIC_API_KEY   # required for Claude planner
        QWEN_API_KEY        # required for Qwen planner
        COMPUTER_USE_OOTB_ROOT  # optional, repo location

    All other ``**override_kwargs`` are forwarded to the bridge constructor.
    """
    return ComputerUseOotbBridge(
        planner_model=planner_model,
        actor_model=actor_model,
        **override_kwargs,
    )


